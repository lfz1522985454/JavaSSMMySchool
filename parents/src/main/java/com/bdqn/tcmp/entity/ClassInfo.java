package com.bdqn.tcmp.entity;

import java.io.Serializable;

/**
 * 班级
 *
 */
public class ClassInfo implements Serializable{

	private int classId;
	private String className;
	private int teacherId;
	private String teacherName;
	private int headTeacherId;
	private String headTeacherName;
	private int isEnabled;
	private String remark;
	private int stuCount;


	public int getStuCount() {
		return stuCount;
	}
	public void setStuCount(int stuCount) {
		this.stuCount = stuCount;
	}
	public int getClassId() {
		return classId;
	}
	public void setClassId(int classId) {
		this.classId = classId;
	}
	public String getClassName() {
		return className;
	}
	public void setClassName(String className) {
		this.className = className;
	}
	public int getTeacherId() {
		return teacherId;
	}
	public void setTeacherId(int teacherId) {
		this.teacherId = teacherId;
	}
	public String getTeacherName() {
		return teacherName;
	}
	public void setTeacherName(String teacherName) {
		this.teacherName = teacherName;
	}
	public int getHeadTeacherId() {
		return headTeacherId;
	}
	public void setHeadTeacherId(int headTeacherId) {
		this.headTeacherId = headTeacherId;
	}
	public String getHeadTeacherName() {
		return headTeacherName;
	}
	public void setHeadTeacherName(String headTeacherName) {
		this.headTeacherName = headTeacherName;
	}

	public int getIsEnabled() {
		return isEnabled;
	}
	public void setIsEnabled(int isEnabled) {
		this.isEnabled = isEnabled;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}

	public String toString() {
		return "ClassInfo [classId=" + classId + ", className=" + className + ", teacherId=" + teacherId
				+ ", teacherName=" + teacherName + ", headTeacherId=" + headTeacherId + ", headTeacherName="
				+ headTeacherName + ", isEnabled=" + isEnabled + ", remark=" + remark + ", stuCount=" + stuCount + "]";
	}



}

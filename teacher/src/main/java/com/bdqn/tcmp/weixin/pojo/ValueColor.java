package com.bdqn.tcmp.weixin.pojo;

import com.bdqn.tcmp.weixin.enums.EnumColor;

public class ValueColor {

    private String value;
    private EnumColor color;

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    public EnumColor getColor() {
        return color;
    }

    public void setColor(EnumColor color) {
        this.color = color;
    }

    public ValueColor(String value, EnumColor color) {
        this.value = value;
        this.color = color;
    }

    public ValueColor() {
    }
}

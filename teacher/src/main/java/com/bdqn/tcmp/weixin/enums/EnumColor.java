package com.bdqn.tcmp.weixin.enums;

public enum EnumColor {

    /**
     * 橙色
     */
    ORANGE("#FFA500"),
    /**
     * 金色
     */
    GOLD("#FFD700"),
    /**
     * 蓝色
     */
    BLUE("#0000FF"),
    /**
     * 黑色
     */
    BLACK("#000000"),
    /**
     * 红色
     */
    RED("#FF0000");
    private String name;

    private EnumColor(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }
}

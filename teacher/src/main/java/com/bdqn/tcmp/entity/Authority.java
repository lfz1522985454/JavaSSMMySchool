package com.bdqn.tcmp.entity;

import java.io.Serializable;

public class Authority implements Serializable{
    //权限Id
    private Integer authorityId;
    //权限类型
    private String  authorityType;
    //权限名称
    private String authorityName;
    //权限路径
    private String authorityPath;
    //权限描述
    private String authorityDesc;
    //权限父级Id
    private Integer parentId;
    //是否启用
    private Integer isEnable;

    public Authority() {
    }

    @Override
    public String toString() {
        return "Authority{" +
                "authorityId=" + authorityId +
                ", authorityType='" + authorityType + '\'' +
                ", authorityName='" + authorityName + '\'' +
                ", authorityPath='" + authorityPath + '\'' +
                ", authorityDesc='" + authorityDesc + '\'' +
                ", parentId=" + parentId +
                ", isEnable=" + isEnable +
                '}';
    }

    public Authority(Integer authorityId, String authorityType, String authorityName, String authorityPath, String authorityDesc, Integer parentId, Integer isEnable) {
        this.authorityId = authorityId;
        this.authorityType = authorityType;
        this.authorityName = authorityName;
        this.authorityPath = authorityPath;
        this.authorityDesc = authorityDesc;
        this.parentId = parentId;
        this.isEnable = isEnable;
    }

    public Integer getAuthorityId() {
        return authorityId;
    }

    public void setAuthorityId(Integer authorityId) {
        this.authorityId = authorityId;
    }

    public String getAuthorityType() {
        return authorityType;
    }

    public void setAuthorityType(String authorityType) {
        this.authorityType = authorityType;
    }

    public String getAuthorityName() {
        return authorityName;
    }

    public void setAuthorityName(String authorityName) {
        this.authorityName = authorityName;
    }

    public String getAuthorityPath() {
        return authorityPath;
    }

    public void setAuthorityPath(String authorityPath) {
        this.authorityPath = authorityPath;
    }

    public String getAuthorityDesc() {
        return authorityDesc;
    }

    public void setAuthorityDesc(String authorityDesc) {
        this.authorityDesc = authorityDesc;
    }

    public Integer getParentId() {
        return parentId;
    }

    public void setParentId(Integer parentId) {
        this.parentId = parentId;
    }

    public Integer getIsEnable() {
        return isEnable;
    }

    public void setIsEnable(Integer isEnable) {
        this.isEnable = isEnable;
    }
}

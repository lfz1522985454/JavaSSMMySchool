package com.bdqn.tcmp.entity;

import java.io.Serializable;

public class Opictype implements Serializable{
	private int opicTypeId;
	private String opicTypeName;
	public int getOpicTypeId() {
		return opicTypeId;
	}
	public void setOpicTypeId(int opicTypeId) {
		this.opicTypeId = opicTypeId;
	}
	public String getOpicTypeName() {
		return opicTypeName;
	}
	public void setOpicTypeName(String opicTypeName) {
		this.opicTypeName = opicTypeName;
	}

	public String toString() {
		return "Opictype [opicTypeId=" + opicTypeId + ", opicTypeName=" + opicTypeName + "]";
	}
	
}

package com.bdqn.tcmp.dto;

/**
 * 成绩群发的数据传输对象
 */
public class ResultMessDTO {
    private Integer studentId;
    private String studentName;
    private Integer currentScore;
    private Integer currentRank;
    private Integer lastScore;
    private Integer lastRank;

    public ResultMessDTO() {
    }

    public ResultMessDTO(Integer studentId, String studentName, Integer currentScore, Integer currentRank, Integer lastScore, Integer lastRank) {
        this.studentId = studentId;
        this.studentName = studentName;
        this.currentScore = currentScore;
        this.currentRank = currentRank;
        this.lastScore = lastScore;
        this.lastRank = lastRank;
    }

    public Integer getStudentId() {
        return studentId;
    }

    public void setStudentId(Integer studentId) {
        this.studentId = studentId;
    }

    public String getStudentName() {
        return studentName;
    }

    public void setStudentName(String studentName) {
        this.studentName = studentName;
    }

    public Integer getCurrentScore() {
        return currentScore;
    }

    public void setCurrentScore(Integer currentScore) {
        this.currentScore = currentScore;
    }

    public Integer getCurrentRank() {
        return currentRank;
    }

    public void setCurrentRank(Integer currentRank) {
        this.currentRank = currentRank;
    }

    public Integer getLastScore() {
        return lastScore;
    }

    public void setLastScore(Integer lastScore) {
        this.lastScore = lastScore;
    }

    public Integer getLastRank() {
        return lastRank;
    }

    public void setLastRank(Integer lastRank) {
        this.lastRank = lastRank;
    }
}

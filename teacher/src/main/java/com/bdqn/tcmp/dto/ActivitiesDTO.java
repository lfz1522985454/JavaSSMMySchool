package com.bdqn.tcmp.dto;

import com.bdqn.tcmp.util.Page;

public class ActivitiesDTO extends Page {
    private String activitiesName;
    private String teacherName;
    private String visibleLevel;

    public String getActivitiesName() {
        return activitiesName;
    }

    public void setActivitiesName(String activitiesName) {
        this.activitiesName = activitiesName;
    }

    public String getTeacherName() {
        return teacherName;
    }

    public void setTeacherName(String teacherName) {
        this.teacherName = teacherName;
    }

    public String getVisibleLevel() {
        return visibleLevel;
    }

    public void setVisibleLevel(String visibleLevel) {
        this.visibleLevel = visibleLevel;
    }

    @Override
    public String toString() {
        return "ActivitiesDTO{" +
                "activitiesName='" + activitiesName + '\'' +
                ", teacherName='" + teacherName + '\'' +
                ", visibleLevel='" + visibleLevel + '\'' +
                '}';
    }
}

package com.bdqn.tcmp.dto;

import java.io.Serializable;

public class StudentApprovalDTO implements Serializable{
    private Integer teacherId;
    private Integer headTeacherId;
    private String studentName;

    public StudentApprovalDTO() {
    }

    public StudentApprovalDTO(Integer teacherId, Integer headTeacherId, String studentName) {
        this.teacherId = teacherId;
        this.headTeacherId = headTeacherId;
        this.studentName = studentName;
    }

    public Integer getTeacherId() {
        return teacherId;
    }

    public void setTeacherId(Integer teacherId) {
        this.teacherId = teacherId;
    }

    public Integer getHeadTeacherId() {
        return headTeacherId;
    }

    public void setHeadTeacherId(Integer headTeacherId) {
        this.headTeacherId = headTeacherId;
    }

    public String getStudentName() {
        return studentName;
    }

    public void setStudentName(String studentName) {
        this.studentName = studentName;
    }
}

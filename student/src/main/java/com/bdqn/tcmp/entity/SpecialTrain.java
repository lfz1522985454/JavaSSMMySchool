package com.bdqn.tcmp.entity;

import java.io.Serializable;

public class SpecialTrain implements Serializable {

	private Integer specialTrainId;
	private Integer skillId;
	private Integer count;
	private Integer correctCount;
	private Integer studentId;
	private String studentName;
	private Integer classId;
	private String className;
	private Integer sumCount;// 总答题数
	private Integer sumCorrectCount;// 总答对题数
	private Integer sumSkillIdCount;// 该知识点总答题数
	private Integer skillIdcount;// 该知识点总答题数 可变

	public String getClassName() {
		return className;
	}

	public void setClassName(String className) {
		this.className = className;
	}

	public Integer getSkillIdcount() {
		return skillIdcount;
	}

	public void setSkillIdcount(Integer skillIdcount) {
		this.skillIdcount = skillIdcount;
	}

	public Integer getSumSkillIdCount() {
		return sumSkillIdCount;
	}

	public void setSumSkillIdCount(Integer sumSkillIdCount) {
		this.sumSkillIdCount = sumSkillIdCount;
	}

	public String getStudentName() {
		return studentName;
	}

	public void setStudentName(String studentName) {
		this.studentName = studentName;
	}

	public Integer getSumCount() {
		return sumCount;
	}

	public void setSumCount(Integer sumCount) {
		this.sumCount = sumCount;
	}

	public Integer getSumCorrectCount() {
		return sumCorrectCount;
	}

	public void setSumCorrectCount(Integer sumCorrectCount) {
		this.sumCorrectCount = sumCorrectCount;
	}

	public Integer getClassId() {
		return classId;
	}

	public void setClassId(Integer classId) {
		this.classId = classId;
	}

	public Integer getStudentId() {
		return studentId;
	}

	public void setStudentId(Integer studentId) {
		this.studentId = studentId;
	}

	public Integer getSpecialTrainId() {
		return specialTrainId;
	}

	public void setSpecialTrainId(Integer specialTrainId) {
		this.specialTrainId = specialTrainId;
	}

	public Integer getSkillId() {
		return skillId;
	}

	public void setSkillId(Integer skillId) {
		this.skillId = skillId;
	}

	public Integer getCount() {
		return count;
	}

	public void setCount(Integer count) {
		this.count = count;
	}

	public Integer getCorrectCount() {
		return correctCount;
	}

	public void setCorrectCount(Integer correctCount) {
		this.correctCount = correctCount;
	}

	public SpecialTrain(Integer specialTrainId, Integer skillId, Integer count, Integer correctCount, Integer studentId,
			String studentName, Integer classId, String className, Integer sumCount, Integer sumCorrectCount,
			Integer sumSkillIdCount, Integer skillIdcount) {
		super();
		this.specialTrainId = specialTrainId;
		this.skillId = skillId;
		this.count = count;
		this.correctCount = correctCount;
		this.studentId = studentId;
		this.studentName = studentName;
		this.classId = classId;
		this.className = className;
		this.sumCount = sumCount;
		this.sumCorrectCount = sumCorrectCount;
		this.sumSkillIdCount = sumSkillIdCount;
		this.skillIdcount = skillIdcount;
	}

	public SpecialTrain() {
		super();
		// TODO Auto-generated constructor stub
	}

}
